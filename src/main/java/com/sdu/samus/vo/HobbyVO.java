package com.sdu.samus.vo;

public class HobbyVO {
	private String art;
	private String cartoon;
	private String food;
	private String idol;
	private String game;
	private String movie;
	private String music;
	private String reading;
	private String sport;
	private String travel;

	public HobbyVO(){}

	public HobbyVO(String art, String cartoon, String food, String idol, String game, String movie, String music, String reading, String sport, String travel) {
		this.art = art;
		this.cartoon = cartoon;
		this.food = food;
		this.idol = idol;
		this.game = game;
		this.movie = movie;
		this.music = music;
		this.reading = reading;
		this.sport = sport;
		this.travel = travel;
	}


	public String getArt() {
		return art;
	}

	public void setArt(String art) {
		this.art = art;
	}

	public String getCartoon() {
		return cartoon;
	}

	public void setCartoon(String cartoon) {
		this.cartoon = cartoon;
	}

	public String getFood() {
		return food;
	}

	public void setFood(String food) {
		this.food = food;
	}

	public String getIdol() {
		return idol;
	}

	public void setIdol(String idol) {
		this.idol = idol;
	}

	public String getGame() {
		return game;
	}

	public void setGame(String game) {
		this.game = game;
	}

	public String getMovie() {
		return movie;
	}

	public void setMovie(String movie) {
		this.movie = movie;
	}

	public String getMusic() {
		return music;
	}

	public void setMusic(String music) {
		this.music = music;
	}

	public String getReading() {
		return reading;
	}

	public void setReading(String reading) {
		this.reading = reading;
	}

	public String getSport() {
		return sport;
	}

	public void setSport(String sport) {
		this.sport = sport;
	}

	public String getTravel() {
		return travel;
	}

	public void setTravel(String travel) {
		this.travel = travel;
	}







}
